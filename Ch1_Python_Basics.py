#Expression
'''
Expression contains operators & values, and would evaluate to a single value
For example, 2 + 2 will evetually become 4, 
2 itself is also an expression, just that it evaluates to 2 itself only
'''
#Statement
'''
Statement contains variable name, assignment operator and a value.
It does not evaluate down to a single value.
'''
a = 2 + 2

#Variables
'''
Variables are the storing boxes for values, 
Variable names can contain letters, numbers and underscore, but cannot start with number
Variable names are case sensitive
'''
a = 10
variable10 = 10

